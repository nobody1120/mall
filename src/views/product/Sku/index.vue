<template>
  <div>
    <el-table
      style="width:100%"
      border
      :data="records"
    >
      <el-table-column
        label="序号"
        type="index"
        align="center"
        width="80"
      ></el-table-column>
      <el-table-column
        label="名称"
        width="width"
        prop="skuName"
      ></el-table-column>
      <el-table-column
        label="描述"
        width="width"
        prop="skuDesc"
      ></el-table-column>
      <el-table-column
        label="默认图片"
        width="130"
      >
        <template slot-scope="{row,$index}">

          <img
            :src="row.skuDefaultImg"
            style="width:100px;height:100px"
          >
        </template>
      </el-table-column>
      <el-table-column
        label="重量"
        width="80"
        prop="weight"
      ></el-table-column>
      <el-table-column
        label="价格"
        width="80"
        prop="price"
      ></el-table-column>
      <el-table-column
        label="操作"
        width="width"
      >
        <template slot-scope="{row,$index}">
          <el-button
            type="success"
            icon="el-icon-bottom"
            size="mini"
            v-if="row.isSale==0"
            @click="sale(row)"
          ></el-button>
          <el-button
            type="success"
            icon="el-icon-top"
            size="mini"
            v-else
            @click="cancel(row)"
          ></el-button>
          <el-button
            type="primary"
            icon="el-icon-edit"
            size="mini"
            @click="edit(row)"
          ></el-button>
          <el-button
            type="info"
            icon="el-icon-info"
            size="mini"
            @click="getSkuInfo(row)"
          ></el-button>
          <el-button
            type="danger"
            icon="el-icon-delete"
            size="mini"
          ></el-button>
        </template>
      </el-table-column>

    </el-table>
    <!-- 分页 -->
    <el-pagination
      style="margin-top:20px ;text-align: center;"
      :total="total"
      :current-page="page"
      :page-size="limit"
      :page-count="7"
      :page-sizes="[3,5,10]"
      layout="prev,pager,next ,jumper,->,sizes,total"
      @current-change="getSkuList"
      @size-change="handleSizeChange"
    ></el-pagination>
    <el-drawer
      :visible.sync="drawer"
      :show-close="false"
      :before-close="handleClose"
      size="50%"
    >
      <el-row>
        <el-col :span="5">
          名称
        </el-col>
        <el-col :span="16">
          {{skuInfo.skuName}}
        </el-col>
      </el-row>
      <el-row>
        <el-col :span="5">
          描述
        </el-col>
        <el-col :span="16">
          {{skuInfo.skuDesc}}
        </el-col>
      </el-row>
      <el-row>
        <el-col :span="5">
          价格
        </el-col>
        <el-col :span="16">
          {{skuInfo.price}}元
        </el-col>
      </el-row>
      <el-row>
        <el-col :span="5">
          平台属性
        </el-col>
        <el-col :span="16">
          <template>
            <el-tag
              type="success"
              v-for="(attr,index) in skuInfo.skuAttrValueList"
              :key="attr.id"
              style="margin-right:10px"
            >{{attr.attrId}}-{{attr.valueId}}</el-tag>
          </template>
        </el-col>
      </el-row>
      <el-row>
        <el-col :span="5">
          商品图片
        </el-col>
        <el-col :span="16">
          <el-carousel height="150px">
            <el-carousel-item
              v-for="item in skuInfo.skuImageList"
              :key="item.id"
            >
              <img :src="item.imgUrl">
            </el-carousel-item>
          </el-carousel>
        </el-col>
      </el-row>

    </el-drawer>
  </div>
</template>

<script>
export default {
  name: 'Sku',
  data() {
    return {
      drawer: false,

      limit: 10,
      page: 1,
      records: [],
      total: 0,
      skuInfo: {},
    };
  },
  mounted() {
    this.getSkuList();
  },
  methods: {
    handleSizeChange(limit) {
      this.limit = limit;
      this.getSkuList();
    },
    async getSkuList(pages = 1) {
      this.page = pages;
      let result = await this.$API.sku.reqSkuList(this.page, this.limit);
      if (result.code == 200) {
        this.total = result.data.total;
        this.records = result.data.records;
      }
    },
    async sale(row) {
      let result = await this.$API.sku.reqSale(row.id);
      if (result.code == 200) {
        row.isSale = 1;
        this.$message({ type: 'success', message: '上架成功' });
      }
    },
    async cancel(row) {
      let result = await this.$API.sku.reqCancel(row.id);
      if (result.code == 200) {
        row.isSale = 0;
        this.$message({ type: 'success', message: '下架成功' });
      }
    },
    edit() {
      this.$message({ type: 'success', message: '正在开发中' });
    },
    async getSkuInfo(row) {
      this.drawer = true;
      let result = await this.$API.sku.reqSkuById(row.id);
      if (result.code == 200) {
        this.skuInfo = result.data;
      }
    },
    handleClose(done) {
      this.$confirm('确认关闭？')
        .then((_) => {
          done();
        })
        .catch((_) => {});
    },
  },
};
</script>

